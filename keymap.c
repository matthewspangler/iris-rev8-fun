layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
        case _QWERTY:
            rgb_matrix_set_color_all(0,0,0);
            rgb_matrix_set_color_all(255,0,0);
            break;
        case _FN:
            rgb_matrix_set_color_all(0,0,0);
            rgb_matrix_set_color_all(255,0,0);
            break;
        case _SYM:
            rgb_matrix_set_color_all(0,0,0);
            rgb_matrix_set_color_all(255,0,0);
            break;
        case _NAV:
            rgb_matrix_set_color_all(0,0,0);
            rgb_matrix_set_color_all(255,0,0);
            break;
        case _MOUSE:
            rgb_matrix_set_color_all(0,0,0);
            rgb_matrix_set_color_all(255,0,0);
            break;
        case _MEDIA:
            rgb_matrix_set_color_all(0,0,0);
            rgb_matrix_set_color_all(255,0,0);
            break;
        case _GAMING:
            rgb_matrix_set_color_all(0, 0, 0);
            rgb_matrix_set_color_all(0, 0, 255);
            break;
        default:
            rgb_matrix_set_color_all(0,0,0);
            rgb_matrix_set_color_all(255,0,0);
            break;
    }
    return state;
}
